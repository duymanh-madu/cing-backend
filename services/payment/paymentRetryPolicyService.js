/**
 * =====================================================
 * CONFIG
 * =====================================================
 */

const MAX_RETRY =
  3;

/**
 * =====================================================
 * CAN RETRY
 * =====================================================
 */

function canRetryPayment({

  retry_count,

}) {

  return (
    retry_count <
    MAX_RETRY
  );

}

/**
 * =====================================================
 * NEXT RETRY DELAY
 * =====================================================
 */

function getRetryDelay(
  retry_count
) {

  return (
    retry_count *
    5000
  );

}

/**
 * =====================================================
 * EXPORTS
 * =====================================================
 */

module.exports = {

  canRetryPayment,

  getRetryDelay,

  MAX_RETRY,

};